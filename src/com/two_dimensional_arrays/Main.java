package com.two_dimensional_arrays;

public class Main {
    public static void main(String[] args) {
        for (int outer = 0; outer < 4; outer ++) {
            System.out.println("Outer index : " + outer);

            for (int inner = 0; inner < 5; inner ++) {
                System.out.println("Inner index : " + inner);
            }
        }
    }
}
